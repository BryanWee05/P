## int() function ##

#Convert string into integer
num_strings = "30"
num_int=int(num_strings)
print(type(num_int))

#Convert float into integer
num_float= 30.65
num_int2=int(num_float)
print(num_int2)
print(type(num_int2))

## float() function ##

#Convert string(float) into float (float literal)
num_strings2 = "30.5"
num_float2 = float(num_strings2)
print(type(num_float2))

#Convert integer into float
num_int3 = 30
num_float3 = float(num_int3)
print(type(num_float3))

## Reduce the numbers of zeros with exponent symbol 'e' ##
print(1e9)
